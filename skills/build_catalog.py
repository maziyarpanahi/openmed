        "deid = openmed.deidentify(note, policy=\"hipaa_safe_harbor\")   # PHI removed on-device\n"
        "meds = openmed.analyze_text(deid.deidentified_text,\n"
        "                            model_name=\"pharma_detection_superclinical\")\n"
