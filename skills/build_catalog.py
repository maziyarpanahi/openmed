        "| **OpenAI Codex** | `./install-skills.sh codex` | `~/.codex/skills/` |\n"
        "| **OpenCode** | `./install-skills.sh opencode` | `~/.config/opencode/skills/` |\n"
        "| **Any other agent** | `./install-skills.sh agents` | `~/.agents/skills/` |\n\n"
